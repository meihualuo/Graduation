package com.example.mazigame.model;

public class CubeModel {
    private int weighe;
    private int heighe;

    public CubeModel(int weighe, int heighe) {
        this.weighe = weighe;
        this.heighe = heighe;
    }

    public int getWeighe() {
        return weighe;
    }

    public void setWeighe(int weighe) {
        this.weighe = weighe;
    }

    public int getHeighe() {
        return heighe;
    }

    public void setHeighe(int heighe) {
        this.heighe = heighe;
    }
}
